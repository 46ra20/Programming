#include<bits/stdc++.h>
using namespace std;
int main(){
    long long int x,y;
    cin>>x>>y;
    long long int ans = x+y;
    cout<<x<<" + "<<y<<" = "<<x+y<<endl;
    ans = x*y;
    cout<<x<<" * "<<y<<" = "<<x*y<<endl;
    ans = x-y;
    cout<<x<<" - "<<y<<" = "<<x-y<<endl;
    return 0;
}

